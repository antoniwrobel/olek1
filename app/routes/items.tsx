import type { LoaderFunction } from "@remix-run/node";
import { json } from "@remix-run/node";
import { Form, Link, NavLink, Outlet, useLoaderData } from "@remix-run/react";
import { useOptionalUser, useUser } from "~/utils";
import { getItemsList } from "~/models/item.server";
import { requireUserId } from "~/session.server";
import { checkIfIsAdmin } from "~/models/user.server";

type LoaderData = {
  itemsList: Awaited<ReturnType<typeof getItemsList>>;
};

export const loader: LoaderFunction = async ({ request }) => {
  const userId = await requireUserId(request);

  const isAdmin = await checkIfIsAdmin(userId);

  if (!isAdmin) {
    throw new Response("Not Authorized", { status: 401 });
  }

  const itemsList = await getItemsList();
  return json<LoaderData>({ itemsList });
};

export default function NotesPage() {
  const data = useLoaderData() as LoaderData;
  const user = useOptionalUser();

  return (
    <div className="flex h-full min-h-screen flex-col">
      <header className="flex items-center justify-between bg-slate-800 p-4 text-white">
        <h1 className="text-3xl font-bold">
          <Link to="/">Items</Link>
        </h1>
        <p>{user?.email}</p>
        <Form action="/logout" method="post">
          <button
            type="submit"
            className="rounded bg-slate-600 py-2 px-4 text-blue-100 hover:bg-blue-500 active:bg-blue-600"
          >
            Logout
          </button>
        </Form>
      </header>

      <main className="flex h-full bg-white">
        <div className="h-full w-80 border-r bg-gray-50">
          <Link to="new" className="block p-4 text-xl text-blue-500">
            + Add new item
          </Link>
          <hr />

          {data.itemsList.allParents.length === 0 ? (
            <p className="p-4">No items yet</p>
          ) : (
            <ol>
              {data.itemsList.allParents.map((item) => {
                return (
                  <li key={item.id}>
                    <NavLink
                      className={({ isActive }) =>
                        `block border-b p-4 text-xl ${
                          isActive ? "bg-white" : ""
                        }`
                      }
                      to={item.id}
                    >
                      üìù {item.name}
                    </NavLink>
                  </li>
                );
              })}
            </ol>
          )}
        </div>

        <div className="flex-1 p-6">
          <Outlet />
        </div>
      </main>
    </div>
  );
}
